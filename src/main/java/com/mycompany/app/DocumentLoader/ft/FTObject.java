package com.mycompany.app.DocumentLoader.ft;

class FTObject {

    private String docNo;
    private String profile;
    private String date;
    private String headline;
    private String pub;
    private String page;
    private String byLine;
    private String dateLine;
    private String text;

    FTObject() {
        super();
    }

    String getDocNo() {
        return docNo;
    }

    void setDocNo(String docNo) {
        this.docNo = docNo;
    }

    String getProfile() {
        return profile;
    }

    void setProfile(String profile) {
        this.profile = profile;
    }

    String getDate() {
        return date;
    }

    void setDate(String date) {
        this.date = date;
    }

    String getHeadline() {
        return headline;
    }

    void setHeadline(String headline) {
        this.headline = headline;
    }

    String getPub() {
        return pub;
    }

    void setPub(String pub) {
        this.pub = pub;
    }

    String getPage() {
        return page;
    }

    void setPage(String page) {
        this.page = page;
    }

    String getByLine() {
        return byLine;
    }

    void setByLine(String byLine) {
        this.byLine = byLine;
    }

    String getDateLine() {
        return dateLine;
    }

    void setDateLine(String dateLine) {
        this.dateLine = dateLine;
    }

    String getText() {
        return text;
    }

    void setText(String text) {
        this.text = text;
    }

    
}